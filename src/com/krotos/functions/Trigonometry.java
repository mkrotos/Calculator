package com.krotos.functions;

public class Trigonometry {

    static public double sin(double a){
        return Math.sin(a);
    }
    static public double cos(double a){
        return Math.cos(a);
    }
    static public double tan(double a){
        return Math.tan(a);
    }
    static public double ctan(double a){
        return ctan(a);
    }

}
